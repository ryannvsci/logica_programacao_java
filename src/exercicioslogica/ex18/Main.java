package exercicioslogica.ex18;

public class Main {
    public static void main(String[] args) {
        /*
         * Exercício 18
         * Faça um algoritmo que imprima na tela a tabuada de 1 até 10.
         */

        for (int i = 1; i <= 10; i++) {
            System.out.println("9 x " + i + " = " +(i * 9));
        }
    }
}
